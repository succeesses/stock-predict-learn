def _normalize(code: str) -> str:
    c = code.strip().upper()
    for pf in ["SH", "SZ", "BJ"]:
        if c.startswith(pf) and c[len(pf):].isdigit():
            return c[len(pf):]
    if "." in c:
        c = c.split(".")[0]
    return c


def fetch_live_csi300(timeout: int = 15) -> Optional[list[str]]:
    """从 AKShare 获取最新 CSI300 成分股列表（300 只精确匹配）。

    需要安装 akshare: pip install akshare
    失败时返回 None，降级到内置兜底列表。
    """
    global _live_cache, _live_set
    try:
        import akshare as ak
        df = ak.index_stock_cons(symbol="000300")
        raw = [str(c).strip().zfill(6) for c in df.iloc[:, 0].tolist()]
        codes = sorted(set(raw))
        _live_cache = codes
        _live_set = frozenset(codes)
        logger.info(f"[StockList] 实时获取 CSI300: {len(codes)} 只")
        return codes
    except ImportError:
        logger.debug("[StockList] akshare 未安装，使用内置列表")
    except Exception as e:
        logger.warning(f"[StockList] 获取 CSI300 失败: {e}，使用内置列表")
    return None


def get_csi300(prefer_live: bool = True) -> list[str]:
    """返回 CSI300 成分股列表。

    Args:
        prefer_live: 是否优先从 AKShare 实时获取（默认 True）
    """
    if prefer_live:
        live = fetch_live_csi300()
        if live:
            return list(live)
    return list(_CSI300_FALLBACK)


def is_csi300(code: str) -> bool:
    """判断代码是否属于 CSI300 成分股。"""
    c = _normalize(code)
    if _live_set:
        return c in _live_set
    return c in _FALLBACK_SET


def get_stock_list(source: str = "csi300") -> list[str]:
    """获取成分股列表。

    Args:
        source: "csi300" / "custom:code1,code2,..."

    Returns:
        6 位数字股票代码列表
    """
    if source == "csi300":
        return get_csi300(prefer_live=True)
    elif source.startswith("custom:"):
        raw = source.split(":", 1)[1]
        return [c.strip() for c in raw.split(",") if c.strip()]
    raise ValueError(f"未知成分股来源: {source}")


def validate_codes(codes: list[str]) -> dict[str, str]:
    """校验股票代码格式，返回 {code: 错误信息或空字符串}。"""
    result = {}
    for code in codes:
        c = _normalize(code)
        if not c.isdigit():
            result[code] = "代码包含非数字字符"
        elif len(c) != 6:
            result[code] = f"长度 {len(c)}，应为 6"
        else:
            result[code] = ""
    return result
